CREATE TABLE jatekok ( 
    sorszam VARCHAR(5) NOT NULL,
    evszam INT NOT NULL,
    varos VARCHAR(14) NOT NULL,
    orszag VARCHAR(14) NOT NULl,
    PRIMARY KEY (sorszam)
);

CREATE TABLE olimpikonok ( 
    id INT NOT NULL,
    nev VARCHAR(75) NOT NULL,
    PRIMARY KEY (id)
);
CREATE TABLE sportagak ( 
    id INT NOT NULL,
    sportag VARCHAR(28) NOT NULL UNIQUE,
    PRIMARY KEY (id)
);

CREATE TABLE versenyszamok ( 
    id INT NOT NULL,
    versenyszam VARCHAR(28) NOT NULL UNIQUE,
    sportagId INT NOT NULL,
    PRIMARY KEY (id),
    FOREIGN KEY (sportagId) REFERENCES sportagak(id)
);

CREATE TABLE eredmenyek ( 
    id INT NOT NULL,
    jatekId VARCHAR(5) NOT NULL,
    olimpikonId INT NOT NULL,
    helyezes INT NOT NULL,
    versenyszamId INT NOT NULL,
    PRIMARY KEY (id),
    FOREIGN KEY (jatekId) REFERENCES jatekok(sorszam),
    FOREIGN KEY (olimpikonId) REFERENCES olimpikonok(id),
    FOREIGN KEY (versenyszamId) REFERENCES versenyszamok(id)
);

